// Probe every track's MP3 with ffprobe and emit src/durations.ts — a static
// { trackId: seconds } map consumed by track-meta.ts so MusicRecording JSON-LD
// carries an ACCURATE duration (was a hardcoded, wrong PT3M30S for every track).
//
// MANUAL script (not in prebuild): ffprobe isn't available on CF Workers Builds,
// so the generated src/durations.ts is committed. Re-run after adding tracks:
//   node --experimental-strip-types scripts/gen-durations.mjs
import { execFileSync } from 'node:child_process';
import { writeFileSync, existsSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { TRACKS } from '../src/data.ts';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');

function probe(file) {
  const path = resolve(ROOT, 'public' + file);
  if (!existsSync(path)) return null;
  try {
    const out = execFileSync(
      'ffprobe',
      ['-v', 'error', '-show_entries', 'format=duration', '-of', 'csv=p=0', path],
      { encoding: 'utf-8' }
    ).trim();
    const secs = Math.round(parseFloat(out));
    return Number.isFinite(secs) && secs > 0 ? secs : null;
  } catch {
    return null;
  }
}

const map = {};
let ok = 0;
let miss = 0;
for (const t of TRACKS) {
  const secs = probe(t.file);
  if (secs) {
    map[t.id] = secs;
    ok += 1;
  } else {
    miss += 1;
    console.warn('  ⚠ no duration for', t.id, t.file);
  }
}

const body = `// AUTO-GENERATED by scripts/gen-durations.mjs — do not edit by hand.
// Track durations in whole seconds, probed from the source MP3s via ffprobe.
// Consumed by track-meta.ts for accurate MusicRecording JSON-LD duration.
export const TRACK_DURATIONS: Record<string, number> = ${JSON.stringify(map, null, 2)};
`;
writeFileSync(resolve(ROOT, 'src/durations.ts'), body);
console.log(`src/durations.ts written — ${ok} durations, ${miss} missing.`);
